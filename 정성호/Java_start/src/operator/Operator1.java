package operator;

public class Operator1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;

        int sum = a + b;
        System.out.println("a + b = " + sum); // 덧셈

        int diff = a - b;
        System.out.println("a - b = " + diff); // 뺼셈

        int multi = a * b;
        System.out.println("a * b = " + multi); // 곱셈

        int div = a / b;
        System.out.println("a / b = " + div); // 나눗셈

        int mod = a % b;
        System.out.println("a % b = " + mod); // 나머지

    }
}
